public class cuenta {

    String titular;
    double  cantidad;

    public cuenta(){
        titular="Wilson";
        cantidad=1500;
    }




    public String getTitular() {
        return titular;
    }

    public void setTitular(String titular) {
        this.titular = titular;
    }

    public double getCantidad() {
        return cantidad;
    }

    public void setCantidad(double cantidad) {
        this.cantidad = cantidad;
    }
}
